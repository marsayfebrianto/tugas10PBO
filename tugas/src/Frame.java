    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        try {
            JFileChooser fileChooser = new JFileChooser();
            int result = fileChooser.showSaveDialog(this);
            if (result == JFileChooser.APPROVE_OPTION) {
                String fileName = fileChooser.getSelectedFile().getAbsolutePath();
                FileWriter writer = new FileWriter(fileName);
                writer.write("Hasil proses text"+System.lineSeparator()+"============"+System.lineSeparator());
                writer.write("Jumlah Karakter (tanpa spasi) : "+karakterTxt.getText()+System.lineSeparator());
                writer.write("Jumlah Baris : "+barisTxt.getText()+System.lineSeparator());
                writer.write("Jumlah Huruf Vokal : "+vokalTxt.getText()+System.lineSeparator());
                writer.write("Jumlah Huruf Konsonan : "+konsonanTxt.getText()+System.lineSeparator());
                writer.close();
                JOptionPane.showMessageDialog(null, "File berhasil disimpan.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }

