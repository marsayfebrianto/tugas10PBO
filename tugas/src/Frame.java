    private void prosesButtonActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        try {
            int wordCount = 0;
            int lineCount = 0;
            int vowelCount = 0;
            int consonantCount = 0;
            String[] lines = jTextArea1.getText().split("\r\n|\r|\n");
            lineCount = lines.length;

            String[] words = jTextArea1.getText().split("\\s+");
            wordCount = words.length;

            for (int i = 0; i < jTextArea1.getText().length(); i++) {
                char ch = jTextArea1.getText().charAt(i);
                if (Character.isLetter(ch)) {
                    if (isVowel(ch)) {
                        vowelCount++;
                    } else {
                        consonantCount++;
                    }
                }
            }
            barisTxt.setText(Integer.toString(lineCount));
            vokalTxt.setText(Integer.toString(vowelCount));
            konsonanTxt.setText(Integer.toString(consonantCount));
            karakterTxt.setText(Integer.toString(wordCount));
            JOptionPane.showMessageDialog(this,"Berhasil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }

    public static boolean isVowel(char ch) {
        ch = Character.toLowerCase(ch);
        return (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u');
    }

